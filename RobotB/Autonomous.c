void intake(bool on)
{
	motor[I2] = motor[I1] = (on) ? (127) : (0);

}


void rest()//all motors on robot stop
{
	freeze();
	fly(0);
	intake(false);
}

task main()
{
	while(true)
	{
		if(VexRT(Btn5U)){
		leftTurn(1);
		wait10Msec(100);
		}
		if(VexRT(Btn6U)){
		rightTurn(1);
		wait10Msec(100);
		}
		if(VexRT(Btn8U)){
		move(2);
		wait10Msec(100);
		}
		if(VexRT(Btn8D)){
		move(-2);
		wait10Msec(100);
		}

		if(VexRT(Btn7U)){
		rightTurn(360);
		}


		if(vexRT(Btn8R)){
		rightTurn(2);
		move(20);
		leftTurn(10);
		move(30);
		intake(true);
		fly(3);
		wait10Msec(1000);
		rest();
		}
	}
}
