int Presets[4] = {0,55, 85,127};

//These variables track the robots position and orientation
//Useful for any future decisions to veer off track or calculate routes
float locX;
float locY;
float rotation;

//Distance is in inches
//Wheels are 4 inches, so one complete rotation moves the bot 4 inches




void freeze()//for the wheels
{
	motor[LFW] = 0;
	motor[LRW] = 0;
	motor[RFW] = 0;
	motor[RRW] = 0;
}



void move(int dist)
{
	int distance = abs(dist);
	if(dist>0){
	motor[LFW] = 127;
	motor[LRW] = 127;
	motor[RFW] = 127;
	motor[RRW] = 127;
	}else{
	motor[LFW] = -127;
	motor[LRW] = -127;
	motor[RFW] = -127;
	motor[RRW] = -127;
	}
	nMotorEncoder[LFW] = 0;
	while(abs(nMotorEncoder[LFW])/627.2<distance/(4.0*PI)){
		wait1Msec(10);
	}

	locX+=cosDegrees(rotation)*distance;
	locY+=sinDegrees(rotation)*distance;
	freeze();
}

void rightTurn (int angle)
{

	motor[LFW] = 127;
	motor[LRW] = 127;
	motor[RFW] = -127;
	motor[RRW] = -127;

	nMotorEncoder[LFW] = 0;
	while(nMotorEncoder[LFW]/627.2<(3.0*(angle/360.0))){
		wait1Msec(10);
	}

	rotation = (rotation + angle) % 360;
	freeze();
}

void leftTurn (int angle)
{

	motor[LFW] = -127;
	motor[LRW] = -127;
	motor[RFW] = 127;
	motor[RRW] = 127;

	nMotorEncoder[LFW] = 0;
	while(abs(nMotorEncoder[LFW])/627.2<(3.0*(angle/360.0))){
		wait1Msec(10);
	}

	rotation = (rotation - angle) % 360;
	freeze();
}





void fly(int speedElement)
{
	if(!((speedElement < 0) || (speedElement > 4)))
	{
		motor[RF] = Presets[speedElement];
		motor[REF] = Presets[speedElement];
		motor[LF] = Presets[speedElement];
	}
}

