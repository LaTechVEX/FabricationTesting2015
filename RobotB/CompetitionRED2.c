void rightTurn (int angle)
{
	nMotorEncoder[LFW] = 0;
	motor[LFW] = 60;
	motor[LRW] = 60;
	motor[RFW] = -60;
	motor[RRW] = -60;


	while(nMotorEncoder[LFW]/627.2<(3.125*(angle/360.0)))
	{
		wait1Msec(10);
	}

	rotation = (rotation + angle) % 360;
	freeze();
}

void leftTurn (int angle)
{
	motor[LFW] = -127;
	motor[LRW] = -127;
	motor[RFW] = 127;
	motor[RRW] = 127;

	nMotorEncoder[LFW] = 0;
	while(abs(nMotorEncoder[LFW])/627.2<(3.125*(angle/360.0)))
	{
		wait1Msec(10);
	}

	rotation = (rotation - angle) % 360;
	freeze();
}

void fly(int speedElement)
{
	if(!((speedElement < 0) || (speedElement > 4)))
	{
		if(speedElement == 0)
			drivespeed = 0;
		else
			drivespeed = Presets[speedElement];
	}
	else
		drivespeed = 0;

	motor[REF] = drivespeed;
	motor[RF] = motor[REF];
	motor[LF] = motor[REF];
}

void flyspeed(int speed)
{
	if(!((speed < -128) || (speed > 127)))
	{
		drivespeed = speed;
	}
	else
		drivespeed = 0;

	motor[REF] = drivespeed;
	motor[RF] = motor[REF];
	motor[LF] = motor[REF];
}

